export function sloggiBrandExpressions(): string {
  return `SLOGGI BRAND VOICE & EXPRESSIONS — Use these naturally when appropriate:

    Brand essence: Liberating true comfort
    Brand belief: Nothing should hold us back
    Brand proposition: Move comfortably through our world
    Love language: Movement

    Core values (embody these in copy):
    - BOLD: self-assured, confident, assertive
    - JOYOUS: positive, energetic
    - AUTHENTIC: real, honest, direct
    - INCLUSIVE: welcoming, intersectional, diverse, championing
    - INNOVATIVE: problem-solving, future-facing

    Tone of voice — write like this:
    - AUTHENTICALLY: straight talking, no euphemisms, say what we see
    - CHAMPIONING: celebrate everyone, raise people up
    - PEER-TO-PEER: we don't "other" the audience — it's about "us", not "you" or "me"
    - JOYFUL: hopeful and positive
    - INCLUSIVELY: we're all in this together

    DO NOT write like this:
    - EXCLUDING: no "hey ladies" or "go girls" — not every woman wears a bra, not every bra wearer is a woman
    - PHONEY MATEY: no forced friendliness like "Hey guys, it's the company that makes your bra here"
    - ASPIRATIONALLY: we are for everyone, not exclusive or elitist
    - HILARIOUSLY: we are global and jokes don't translate well — avoid humour and puns

    General brand phrases:
    - "Move through our world in comfort"
    - "Nothing should hold us back"
    - "Comfort starts right next to your skin"
    - "The freedom to move"
    - "Welcome to the world of sloggi"
    - "Move in comfort"
    - "Comfort that moves with you"
    - "Designed to liberate"
    - "Feel-good comfort, every day"

    For emotional connection:
    - "Because comfort is personal"
    - "More than function — comfort is a feeling"
    - "Be your true self"
    - "Nothing in the way of being who you really are"
    - "Sharing the joy that comes of knowing we can do anything, be anything"

    Comfort platform keywords (use when matching product series):
    - ADAPT: "soft and adaptive comfort", "active adaptiveness", "works with your body as it changes"
    - ZERO: "absolute invisibility", "ultimate comfort", "zero restriction, zero seams, zero marks"
    - EVER: "comfort from within", "smart tech features", "augmenting your body with specific benefits"
    - GO: "all-round comfort", "modern essential style"
    - ORIGINALS: "classic comfort", "iconic to the core", "high quality fabrics, simple flattering cuts"
    - S by sloggi: "premium bodywear", "sleek cuts and rich colours", "sophisticated basics"`;
}

